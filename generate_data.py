def gen_attrs(rating, position, age):
    """Generate realistic attributes based on overall rating and position."""
    mods = POS_MODS.get(position, {})
    attrs = {}
    for attr, mod in mods.items():
        base = rating + mod
        # Add some randomness based on age (younger = slightly more pace, older = more experience)
        if attr == 'pace' and age < 25:
            base += 2
        elif attr == 'pace' and age > 32:
            base -= 3
        if attr in ['passing', 'defending'] and age > 29:
            base += 2
        attrs[attr] = max(10, min(99, base))
    return attrs

def gen_player(team_id, idx, name, position, rating, age):
    """Generate a player object."""
    attrs = gen_attrs(rating, position, age)
    return {
        'id': f'{team_id}{idx}',
        'name': name,
        'position': position,
        'rating': rating,
        'pace': attrs['pace'],
        'shooting': attrs['shooting'],
        'passing': attrs['passing'],
        'dribbling': attrs['dribbling'],
        'defending': attrs['defending'],
        'physical': attrs['physical'],
        'stamina': attrs['stamina'],
        'age': age,
    }

def gen_team(id, name, short_name, color, text_color, continent, flag, att, mid, defn, ovr, players_data):
    """Generate a team object with players."""
    players = []
    for i, (pname, pos, rat, age) in enumerate(players_data, 1):
        players.append(gen_player(id, i, pname, pos, rat, age))
    return {
        'id': id,
        'name': name,
        'shortName': short_name,
        'color': color,
        'textColor': text_color,
        'continent': continent,
        'flag': flag,
        'attackRating': att,
        'midfieldRating': mid,
        'defenseRating': defn,
        'overallRating': ovr,
        'players': players,
    }

